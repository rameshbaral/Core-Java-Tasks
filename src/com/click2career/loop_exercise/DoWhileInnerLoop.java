/*
39. WAP to print “Hello, this is do while inner loop” from inner loop using do while.
 */
package com.click2career.loop_exercise;

public class DoWhileInnerLoop {
    public static void main(String[] args) {
        do {
            do {
                System.out.println("Hello, this is do while inner loop");
            }while (false);
        }while (false);
    }
}
